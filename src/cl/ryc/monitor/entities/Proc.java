package cl.ryc.monitor.entities;

public class Proc {
	
	
	private String Name;
	private Long PID;
	
	public Proc()
	{
		
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public Long getPID() {
		return PID;
	}

	public void setPID(Long pID) {
		PID = pID;
	}
	
	
	
	

}
